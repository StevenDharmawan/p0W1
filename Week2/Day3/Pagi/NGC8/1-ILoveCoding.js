console.log("1. FOR LOOPING\n")
console.log("LOOPING FOR PERTAMA")
for(var i = 1; i <= 20; i++) {
    console.log(`${i} = I love coding`)
}

console.log("\nLOOPING FOR KEDUA")
for(var i = 20; i >= 1; i--) {
    console.log(`${i} = I will become fullstack developer`)
}

console.log("\n2. WHILE LOOPING\n")
console.log("LOOPING WHILE PERTAMA")
var index = 2
while(index <= 20){
    console.log(`${index} = I love coding`)
    index+=2
}

console.log("\nLOOPING WHILE KEDUA")
var index = 20
while(index >= 2){
    console.log(`${index} = I will become fullstack developer`)
    index-=2
}

